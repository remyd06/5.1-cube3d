#include "cub3d.h"

bool	ft_iskey(char c)
{
	if (c == 'N' || c == 'O' || c == 'S' || c == 'W'
		|| c == 'E' || c == 'A' || c == 'F' || c == 'C')
		return (true);
	return (false);
}
